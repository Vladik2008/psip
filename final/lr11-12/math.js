function calculateF(x) {
    // Проверка ограничений (квадратный корень извлекается только из неотрицательных чисел)
    if (x < 0) {
        return "Ошибка: x должен быть >= 0 (под корнем)";
    }
    
    // Проверка деления на ноль
    var sinX = Math.sin(x);
    if (sinX === 0) {
        return "Ошибка: деление на ноль (sin(x) = 0)";
    }
    
    // Расчет по формуле: f = (5x - √x + 10) / sin(x)
    var result = (5 * x - Math.sqrt(x) + 10) / sinX;
    
    return result;
}